//redeclaracion de variables

var firstName; // Undefined
firstName = 'Oscar';
console.log(firstName);

var lastName = "Barajas"; // declarar / asignar
lastName = "Diego"; // reasignar
console.log(lastName);

var secondName = "Ana"; // declarando / asignando
var secondName = "Juana"; // reasignando
console.log(secondName);

// Let
let fruit = 'Apple' // declarar / asignar
fruit = 'Banana'; // reasignar

console.log(fruit);

// Const
const animal = 'dog'; //declara y asigna
animal = 'cat'; //no se puede reasignar en const
console.log(animal);